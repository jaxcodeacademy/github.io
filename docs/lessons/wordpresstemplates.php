<br><br>
<h3>Steps for creating a minimal Wordpress template</h3>

<ul>
	<li>Create a folder in wp-content/themes, for example wp-content/themes/oyova</li>
	<li>Create the following files: index.php and style.css</li>
	<li>Add a screenshot (jpg or png) and name it screenshot</li>
	<li>Create the following files: header.php, footer.php, front-page.php, page.php, archive.php, single.php</li>
	<li>Include the header and footer with get_header() and get_footer() in front-page.php</li>
	<li>Include wp_head() and body_class() in header.php</li>
</ul>


