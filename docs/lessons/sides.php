<h2 class="section-title">Client Side vs. Server Side</h2>
<div class="section-block">                                    
<div class="row">
<div class="col-md-12 col-sm-12 col-xs-12">



<strong>Client Side</strong>: anything your web browser can do is considered client side. A web browser is a piece of software known as a client.<br><br>

<strong>Server side</strong>: means that requests have to go to the web server, process that request, and send it back to the client, aka the web browser.<br><br>

In this course we'll be learning several interpretive programming languages and frameworks.  Some will be Client side and others will be Server side.<br><br>

And they include:<br><br>

1. <strong>HTML</strong>: is client side, meaning you request a web page and the web browser knows how to display it. HTML stands for Hypertext Markup Language, a standardized system for tagging text files to achieve font, color, graphic, and hyperlink effects on World Wide Web pages.<br><br>
2. <strong>Cascading Style Sheets (CSS)</strong>: <strong style="text-decoration:underline;">C</strong>ascading <strong style="text-decoration:underline;">S</strong>tyle <strong style="text-decoration:underline;">S</strong>heets, fondly referred to as CSS, is a simple design language intended to simplify the process of making web pages presentable.
<br><br>
CSS handles the look and feel part of a web page. Using CSS, you can control the color of the text, the style of fonts, the spacing between paragraphs, how columns are sized and laid out, what background images or colors are used, layout designs, variations in display for different devices and screen sizes as well as a variety of other effects.<br><br>

3. <strong>Javascript</strong>: is scripting language that your web browser knows how to interpret and makes your web pages a little more interactive or dynamic. As opposed to a strict HTML web page which is, Static, mean it doesn't do much but display content. In this course we will not be doing much Javascript. We'll do a little so you know what it's about and can see some of it's uses.<br><br>
4. <strong>PHP</strong>: The main language we'll be learning is PHP.  PHP is a server side scripting language. Which means your web browser cannot process this computing language. The web server (thus, server side) will turn PHP into HTML and send it back to the browswer <br><br>
5. <strong>AJAX</strong>: a Javascript framework.<br><br>
6. <strong>Bootstrap</strong>: an HTML, CSS, and Javascript framework<br><br>
7. <strong>SQL</strong>: is a language used to communicate with a database and stands for Structured Query Language.<br><br>
8. <strong>MySQL</strong>: a relational database.<br><br>

<h4>LAMP Stack</h4>
When you open a webhosting account what you essentially do is rent space on a computer, sitting in a data warehouse somewhere in the world. And on this computer is a piece of software know as a web server, it listens for requests from the web and serves up web pages. 
<br><br>The most common web server used today is called <a href="https://httpd.apache.org/" target="_blank">Apache</a>. <a href="https://httpd.apache.org/" target="_blank">Apache</a> is open source.  Any computer program that you can use for free is called open source, which means the code is open and you can do what you want with it.  

<br><br><a href="http://php.net/" target="_blank">PHP</a> is also open source. As is the operating system  <a href="https://www.linux.org/" target="_blank">Linux</a>, which most web servers run on.  If you have an Apple computer, it's operating system, MAC OS, is running on top of Linux. MySQL is also open source.  <a href="https://www.mysql.com/" target="_blank">MySQL</a> is a database that many websites use to collect data.<br><br>

When you group a bunch of software together to use we call it a STACK. There are lots of different combos that make different stacks.  In this course and most likely any job you get will use what's called a LAMP STACK.<br><br>

LAMP stands for <strong style="text-decoration:underline;">L</strong>inux, <strong style="text-decoration:underline;">A</strong>pache, <strong style="text-decoration:underline;">M</strong>ySQL, and <strong style="text-decoration:underline;">P</strong>HP.<br><br>



