<h4>The &lt;div&gt; tag</h4>
When used together with CSS, the &lt;div&gt; element can be used to style blocks of content:
<div class="code-block">
<pre><code class="language-markup">
&#x3C;div style=&#x22;background-color:black;color:white;padding:20px;&#x22;&#x3E;
  &#x3C;h2&#x3E;Jax Code Academy&#x3C;/h2&#x3E;
  &#x3C;p&#x3E;Jax Code Academy is the premiere coding school in Northeast Florida.&#x3C;/p&#x3E;
&#x3C;/div&#x3E;

</code></pre>
</div><!--//code-block-->
