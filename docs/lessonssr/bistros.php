<strong>Project Name</strong>:  Food Ordering System (FOS)<br>
<br>
This project is a continuation of the Bistros assignment.<br>
1. Create a Menu for your Bistros restaurant.<br>
2. Allow online ordering, pickup or delivery<br>
3. Admin Section<br>
4. Customer Section. Customers should be able to order as guest, register, or login as past customer<br>
5. Register a domain name (.info names are cheap). <br>
6. The site must be secured by SSL<br>
<br>
<strong>Language Used</strong>:  PHP<br>
<br>
<strong>Database</strong>:  MySQL<br>
<br>
<strong>User Interface Design</strong>:  HTML, CSS, BOOTSTRAP, AJAX, JQUERY, JAVASCRIPT<br>

<br>
<strong>Food Ordering System using PHP and MySQL</strong><br>
<br>
In FOS we use PHP and MySQL database. In this project any person can order food online. It has two modules, admin and user.<br>
<br>
<strong>Admin Module</strong><br>
<br>
<ul>
	<li>Dashboard: In this section, admin can see all details in brief like total number of orders, not confirmed orders, confirmed orders, total food being prepared, total food pickup, total food deliveries, total cancelled orders, and total user.</li>
	<li>Reg Users: In this section, admin can manage the registered users (view/update).</li>
	<li>Food Categories: In this section, admin can manage the food categorie (Add and Update).</li>
	<li>Food Menu: In this section, admin can manage the food menu (Add and Update).</li>
	<li>Orders: In this section admin can view the food order details and they can change order status according to current status.</li>
	<li>Search order: In this section, admin can search particular order with the help of order number.</li>
	<li>Reports: In this section admin can view order details, order counts and sales reports according to dates.</li>
	<li>Admin can also update their profile, change the password and recover the password.</li>
</ul>

<br>
<strong>User Module</strong><br>
<br>
<ul>
	<li>Food Menu: In this section, user can view which food is available in the restaurant</li>

	<li>My Accounts: In this section, user can change his/her password, view and update his/her profile and log out.</li>

	<li>My orders: In this section, user can view order history after login.</li>

	<li>Cart: In this section, user can add food to their order.</li>

	<li>User can cancel an order.</li>
</ul>

<br>
<strong>Brief Information about the menu page</strong><br>

In this page guest user (user which not registered) can view restaurant's food menu, search food according food name and the guest user can also register and sign in. User can also track their order without login.<br>

